<template>
  <div class="recipe-card">
    <img :src="recipe.recipeImageURLs" class="card-img-top" alt="Recipe Image">
    <div class="card-body">
      <h5 class="card-title">{{ recipe.name }}</h5>
      <p class="card-creator">{{ recipe.creator }}</p>
      <label for = "likeCheckBox">Like</label>
      <input type = "checkbox" id = "likeCheckBox" v-model = "isChecked"  v-on:click = "increaseLikes">
      <!-- Display the number of likes -->
      <p class="likes-count">Likes: {{ recipeLikes }}</p>
      <router-link :to="'/post/' + recipe.autoGeneratedID" class="view-more-button">View More</router-link>
    </div>
  </div>
</template>

<script>
import { getFirestore, collection, addDoc, updateDoc, doc, getDoc } from 'firebase/firestore';

export default {
  props: {
    recipe: Object,
  },

  data(){
    return{
      isChecked: false, // Initialise the checkbox state to false
      recipeLikes: 0 // Initialize number of likes
    }
  },

  async mounted() {
    // Retrieve the checkbox state from localStorage and set it in the data property
    const isChecked = localStorage.getItem(`like_${this.recipe.autoGeneratedID}`);
    if (isChecked) {
      this.isChecked = JSON.parse(isChecked); // Convert it from string into a Javascript object, since localstorage stores strings only
    }

    // Retrive likes for all recipes
    const db = getFirestore();
    // const getRecipe = await getDoc(updateRef); 
    const updateRef = doc(db, "recipes", this.recipe.autoGeneratedID); // Establish reference to recipe in DB
    const getRecipe = await getDoc(updateRef);
    if (getRecipe.exists){
      this.recipeLikes = getRecipe.data().likes;
    }
  },

  methods: {
    async increaseLikes(){
      this.isChecked = !this.isChecked; // Since this triggered after a toggle event, we invert it.

      // Save the checkbox state in localStorage
      localStorage.setItem(`like_${this.recipe.autoGeneratedID}`, JSON.stringify(this.isChecked)); // {RecipeID, True/False}

      // Perform your like/unlike action here
      const db = getFirestore();
      const updateRef = doc(db, "recipes", this.recipe.autoGeneratedID); // Establish reference to recipe in DB


      // Retrieve Recipe Current Likes
      const getRecipe = await getDoc(updateRef); 

      // Retrieve current likes amount
      var recipeLikes = getRecipe.data().likes; 

      // Retrieve user ID that created the recipe
      const userID = getRecipe.data().uid; 

      // Retrieve user current coins
      const userRef = doc(db, "Users", userID)
      const users = await getDoc(userRef);
      var userCoins = users.data().coins;

      // If true, means it was previously false. Increase likes
      if (this.isChecked){
        try{
          recipeLikes += 1;
          await updateDoc(updateRef, {
              likes: recipeLikes
          })

          // Update number of likes in real-time
          this.recipeLikes = recipeLikes;
          console.log(getRecipe.data().autoGeneratedID);
          console.log("Likes updated successfully")

          // Everytime recipe likes increases by 5, user gets 5 additional coins
          if (recipeLikes != 0 && recipeLikes % 5 === 0){
             userCoins += 1;
             await updateDoc(userRef, {
                coins: userCoins
             })
          }
        }
        catch(error){
          console.log("Error message is ", + error);
        }
      }

      // If false, means it was previously true. Reduce likes
      if (!this.isChecked){
        recipeLikes -= 1;
        await updateDoc(updateRef, {
            likes: recipeLikes
          })

        // Update number of likes in real-time
        this.recipeLikes = recipeLikes;
        console.log("Likes removed successfully");
      }
    }
  }
};
</script>

