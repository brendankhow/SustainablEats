<template>
  <div class="recipe-card">
    <!-- Image row -->
    <div class="card-image-container">
      <img :src="recipe.recipeImageURLs + '?timestamp=' + Date.now()" class="card-img-top" alt="Recipe Image">
    </div>
    
    <!-- Title row -->
    <div class="card-row">
      <h5 class="card-title">{{ recipe.name }}</h5>
    </div>
    
    <!-- Creator row -->
    <div class="card-row">
      <p class="card-creator">{{ recipe.creator }}</p>
    </div>
    
    <!-- Likes row -->
    <div class="card-row">
      <p class="likes-count"><strong>{{ recipeLikes }}</strong> Likes</p>
    </div>
    
    <!-- Like and Bookmark buttons row -->
    <div class="card-row button-row">
      <button class="likeButton" value="isChecked" v-on:click="increaseLikes">
        Like <i :class="{'bi bi-heart': !isChecked, 'bi bi-heart-fill heart-icon': isChecked, 'red-heart': isChecked}"></i>
      </button>
      
      <button class="bookmarkButton" value="isBookmarked" v-on:click="bookmarkRecipe">
        Bookmark <i :class="{'bi bi-bookmarks': !isBookmarked, 'bi bi-bookmark-fill bookmark-icon': isBookmarked, 'green-bookmark': isBookmarked}"></i>
      </button>
    </div>
    
    <!-- View More button row -->
    <div class="card-row">
      <router-link :to="'/post/' + recipe.autoGeneratedID" class="view-more-button">View More</router-link>
    </div>
  </div>

  
  <!--Poppins Family Font-->
  <link rel="preconnect" href="https://fonts.googleapis.com">
  <link rel="preconnect" href="https://fonts.gstatic.com" crossorigin>
  <link href="https://fonts.googleapis.com/css2?family=Poppins:ital,wght@0,200;0,300;0,400;0,500;0,600;0,700;0,800;0,900;1,200;1,300;1,400;1,500;1,600;1,700;1,800;1,900&family=Raleway:wght@900&display=swap" rel="stylesheet">
</template>


<script>
import { getFirestore, collection, addDoc, updateDoc, doc, getDoc } from 'firebase/firestore';
import { getAuth } from "firebase/auth";
// import canva confetti
import confetti from "canvas-confetti";

export default {
  props: {
    recipe: Object,
  },

  data(){
    return{
      isChecked: false, // Initialise the checkbox state to false
      recipeLikes: 0, // Initialize number of likes
      isBookmarked: false
    }
  },

  async mounted() {
    // Retrieve the checkbox state from localStorage and set it in the data property
    try{
      const isChecked = localStorage.getItem(`like_${this.recipe.autoGeneratedID}`);
      if (isChecked) {
        this.isChecked = JSON.parse(isChecked); // Convert it from string into a Javascript object, since localstorage stores strings only
      }

      const isBookmarked = localStorage.getItem(`bookmark_${this.recipe.autoGeneratedID}`);
      if (isBookmarked){
        this.isBookmarked = JSON.parse(isBookmarked);
      }

      // Retrive likes for all recipes
      const db = getFirestore();
      // const getRecipe = await getDoc(updateRef); 
      const updateRef = doc(db, "recipes", this.recipe.autoGeneratedID); // Establish reference to recipe in DB
      const getRecipe = await getDoc(updateRef);
      if (getRecipe.exists){
        this.recipeLikes = getRecipe.data().likes;
      }
    }
    
    catch(error){
      console.log("Error message is ", + error);
    }
  },

  methods: {
    async increaseLikes(){
      try{
        confetti({
          particlecount: 400,
          spread: 120
        })
        
        this.isChecked = !this.isChecked; // Since this triggered after a toggle event, we invert it.

        // Save the checkbox state in localStorage
        localStorage.setItem(`like_${this.recipe.autoGeneratedID}`, JSON.stringify(this.isChecked)); // {RecipeID, True/False}

        // Perform your like/unlike action here
        const db = getFirestore();
        const updateRef = doc(db, "recipes", this.recipe.autoGeneratedID); // Establish reference to recipe in DB

        // Retrieve Recipe Current Likes
        const getRecipe = await getDoc(updateRef); 

        // Retrieve current likes amount
        var recipeLikes = getRecipe.data().likes; 

        // Retrieve user ID that created the recipe
        const userID = getRecipe.data().uid; 

        // Retrieve user current coins
        const userRef = doc(db, "Users", userID)
        const users = await getDoc(userRef);
        var userCoins = users.data().coins;

        // If true, means it was previously false. Increase likes
        if (this.isChecked)
        {
          try{
            recipeLikes += 1;
            await updateDoc(updateRef, {
                likes: recipeLikes
            })

            // Update number of likes in real-time
            this.recipeLikes = recipeLikes;
            console.log(getRecipe.data().autoGeneratedID);
            console.log("Likes updated successfully")

            // Everytime recipe likes increases by 5, user gets 5 additional coins
            if (recipeLikes != 0 && recipeLikes % 5 === 0){
              userCoins += 1;
              await updateDoc(userRef, {
                  coins: userCoins
              })
            }
          }
          catch(error){
            console.log("Error message is ", + error);
          }
        }

      // If false, means it was previously true. Reduce likes
        if (!this.isChecked){
          try{
            recipeLikes -= 1;
            await updateDoc(updateRef, {
                likes: recipeLikes
              })

            // Update number of likes in real-time
            this.recipeLikes = recipeLikes;
            console.log("Likes removed successfully");
          }
          catch(error){
            console.log("Error message is " + error);
          }
        }
      }
      catch(error){
        console.log("Error message is " + error);
      }
    },

    async bookmarkRecipe(){
      try{
          this.isBookmarked = !this.isBookmarked;
          localStorage.setItem(`bookmark_${this.recipe.autoGeneratedID}`, JSON.stringify(this.isBookmarked)); // {RecipeID, True/False}

          // Perform your bookmark/un-bookmark action here
          const db = getFirestore();
          const auth = getAuth();
          const user = auth.currentUser;
          if (user)
          {
            const userID = user.uid;
            const updateRef = doc(db, "Users", userID); // Establish reference to user in DB
            const getUser = await getDoc(updateRef); 
            const userData = getUser.data();
            if (userData && Array.isArray(userData.bookmarks)) 
            {
              if (this.isBookmarked){
                userData.bookmarks.push(this.recipe.autoGeneratedID);
                await updateDoc(updateRef, userData);
                console.log("Bookmark added successfully");
              }
              
              if (!this.isBookmarked){
                var bookmarkList = userData.bookmarks;
                var index = bookmarkList.indexOf(this.recipe.autoGeneratedID);
                if (index > -1) {
                  bookmarkList.splice(index, 1);
                }
                await updateDoc(updateRef, {
                  bookmarks: bookmarkList
                })
                console.log("Bookmark removed successfully");
              }
            }
            else{
              console.log("No such document!");
            }
          }
        }
      catch(error){
        console.log("Error message is " + error);
      }
    }
}};
</script>

